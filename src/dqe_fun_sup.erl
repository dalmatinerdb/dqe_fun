%%%-------------------------------------------------------------------
%% @doc dqe_fun top level supervisor.
%% @end
%%%-------------------------------------------------------------------

-module(dqe_fun_sup).

-behaviour(supervisor).

%% API
-export([start_link/0]).

%% Supervisor callbacks
-export([init/1]).

-define(SERVER, ?MODULE).

%%====================================================================
%% API functions
%%====================================================================

start_link() ->
    supervisor:start_link({local, ?SERVER}, ?MODULE, []).

%%====================================================================
%% Supervisor callbacks
%%====================================================================

init([]) ->
    dqe_fun:init(),
    {ok, { {one_for_all, 0, 1}, []} }.

